package tddDeitelExercises;

public class Arithmetic {
    public int findSumOfThreeIntegers(int num1, int num2, int num3) {
        int sum = num1 + num2+ num3;
        return sum;

    }

    public int findAverageOf(int num1, int num2, int num3) {
        int average = (num1 + num2 + num3)/3;
        return average;
    }
}
